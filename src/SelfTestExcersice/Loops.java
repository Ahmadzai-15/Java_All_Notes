package SelfTestExcersice;

public class Loops {
    public static void main(String[] args) {


        for(int i =0; i<10; i++){
            System.out.println(i);
        }

        int b=1;
        while(b<15){
            if(b%2==0){
                System.out.println("Even Number"+b);
            }
            b++;
        }
    }
}
